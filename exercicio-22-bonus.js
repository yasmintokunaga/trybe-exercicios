function basketFruit(basket) {
    let fruitCounter = {};
    fruitCounter[basket[0]] = 1;

    for (let index = 1; index < basket.length; index += 1) {
        if(fruitCounter[basket[index]] >= 1) {
            fruitCounter[basket[index]] += 1;
        } else {
            fruitCounter[basket[index]] = 1;
        }
    }
    let print = 'Sua cesta possui: ';

    for (key in fruitCounter) {
        if(fruitCounter[key] > 1){
        print += fruitCounter[key] + ' ' + key + 's' +  ', ';
    } else {
        print += fruitCounter[key] + ' ' + key +  ', ';
    }
    }
    return print;
}

